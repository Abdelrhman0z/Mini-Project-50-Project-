// Create Boxes
const boxesContainer = document.getElementById('project-container')

const createBoxes = (projects) => {
  const div = document.createElement('div')
  div.classList.add('box')
  const { title, description, imageUrl, link, number } = projects
  div.innerHTML = `
        <img src="${imageUrl}" alt="" />
        <h2>${title}</h2>
        <h4>${number}</h4>
      <p>
         ${description}
      </p>
      <a href="${link}"
      target="_blank"><button class="btn">Visit</button></a
      >
  `

  boxesContainer.appendChild(div)
}

projects.map((projects) => createBoxes(projects))


